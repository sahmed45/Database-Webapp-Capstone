using System;
using System.Collections.Generic;
using System.Linq;
using System.Threading.Tasks;
using Microsoft.AspNetCore.Mvc;
using Microsoft.AspNetCore.Mvc.RazorPages;
using Microsoft.EntityFrameworkCore;
using KSUWEBPMOCAP.Data;

namespace KSUWEBPMOCAP.Pages.Profiles.UserProfile.TblFiles
{
    public class DeleteModel : PageModel
    {
        private readonly KSUWEBPMOCAPContext _context;

        public DeleteModel(KSUWEBPMOCAPContext context)
        {
            _context = context;
        }

        [BindProperty]
        public TblFile TblFile { get; set; }

        public async Task<IActionResult> OnGetAsync(int? id)
        {
            if (id == null)
            {
                return NotFound();
            }

            TblFile = await _context.TblFile.FirstOrDefaultAsync(m => m.ID == id);

            if (TblFile == null)
            {
                return NotFound();
            }
            return Page();
        }

        public async Task<IActionResult> OnPostAsync(int? id)
        {
            if (id == null)
            {
                return NotFound();
            }

            TblFile = await _context.TblFile.FindAsync(id);

            if (TblFile != null)
            {
                _context.TblFile.Remove(TblFile);
                await _context.SaveChangesAsync();
            }

            return RedirectToPage("./Index");
        }
    }
}
